package com.telerik.widget.chart.engine.axes.common;

/**
 * Specifies the predefined time interval types.
 */
public enum TimeInterval {

    /**
     * 1 Year.
     */
    YEAR,

    /**
     * 1 Quarter.
     */
    QUARTER,

    /**
     * 1 Month.
     */
    MONTH,

    /**
     * Week in month.
     */
    WEEK,

    /**
     * Week in year.
     */
    WEEK_OF_YEAR,

    /**
     * Day of month.
     */
    DAY,

    /**
     * Day of week.
     */
    DAY_OF_WEEK,

    /**
     * Day of week in month.
     */
    DAY_OF_WEEK_IN_MONTH,

    /**
     * Day of year.
     */
    DAY_OF_YEAR,

    /**
     * 1 Hour.
     */
    HOUR,

    /**
     * 1 Minute.
     */
    MINUTE,

    /**
     * 1 Second.
     */
    SECOND,

    /**
     * 1 Millisecond.
     */
    MILLISECOND,

    /**
     * Total time in milliseconds.
     */
    TIME_IN_MILLIS
}

