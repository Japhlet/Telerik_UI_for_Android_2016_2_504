package com.telerik.widget.chart.visualization.annotations;

/**
 * Lists the possible horizontal alignment options for an {@link ChartAnnotation}.
 */
public enum HorizontalAlignment {

    /**
     * The annotation is aligned at the left side of the bounding rectangle.
     */
    LEFT,

    /**
     * The annotation is aligned in the middle of the bounding rectangle.
     */
    CENTER,

    /**
     * The annotation is aligned at the right side of the bounding rectangle.
     */
    RIGHT
}
