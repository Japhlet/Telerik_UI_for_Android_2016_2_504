package com.telerik.widget.chart.engine.elementTree;

/**
 * Enlists the possible chart tree traversal modes.
 */
public enum TreeTraversalMode {

    /**
     * Depth-first approach is used when the chart tree is traversed.
     */
    DEPTH_FIRST,

    /**
     * Breath-first approach is used when the chart tree is traversed.
     */
    BREADTH_FIRST,
}

