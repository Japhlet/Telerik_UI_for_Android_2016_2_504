package com.telerik.widget.chart.visualization.annotations;

/**
 * Lists the possible vertical alignment options for an {@link ChartAnnotation}.
 */
public enum VerticalAlignment {

    /**
     * The annotation is aligned at the top side of the bounding rectangle.
     */
    TOP,

    /**
     * The annotation is aligned at the bottom of the bounding rectangle.
     */
    BOTTOM,

    /**
     * The annotation is aligned in the middle of the bounding rectangle.
     */
    CENTER
}
