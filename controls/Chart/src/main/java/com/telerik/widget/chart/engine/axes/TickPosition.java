package com.telerik.widget.chart.engine.axes;

/**
 * Enlists the possible positions for a single tick on an {@link com.telerik.widget.chart.visualization.common.Axis}.
 */
public enum TickPosition {

    /**
     * Specifies that a tick is at the first position on the axis.
     */
    FIRST,

    /**
     * Specifies that a tick is at an inner position on the axis.
     */
    INNER,

    /**
     * Specifies that a tick is at the last position on the axis.
     */
    LAST
}
